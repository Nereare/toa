  function updateTime() {
    // The days go from 0 to 364
    let d = parseInt( $("#cal-day").val() );
    // The years go from -700 to 1600 (DR)
    let y = parseInt( $("#cal-year").val() );
    // The time is in minutes and goes from 0 to 1439
    let t = parseInt( $("#cal-time").val() );
    // The sun pattern is a string that is the index to the sunset/rise map
    let s = $("#cal-sun").val();

    // Update time and inheriting variables
    if ( t >= 0 ) {
      if ( Math.floor( t / 1440 ) > 0 ) {
        d += Math.floor( t / 1440 );
        t = t % 1440;
      }
    } else {
      t = Math.abs(t);
      if ( Math.floor( t / 1440 ) > 0 ) {
        d -= Math.floor( t / 1440 );
        t = 1440 - (t % 1440);
      }
    }
    // Update day and inheriting variables
    if ( d >= 0 ) {
      if ( Math.floor( d / 365 ) > 0 ) {
        y += Math.floor( d / 365 );
        d = d % 365;
      }
    } else {
      d = Math.abs(d);
      if ( Math.floor( d / 365 ) > 0 ) {
        y -= Math.floor( d / 365 );
        d = 365 - (d % 1440);
      }
    }
    // Get season
    let season = year[d][3];
    let ss = 0;
    if ( season == "Summer" ) { ss = 1; }
    else if ( season == "Autumn" ) { ss = 2; }
    else if ( season == "Winter" ) { ss = 3; }
    else { ss = 0; }
    // Get time of day
    let tod = "";
    if ( t >= sun[s][ss][1] ) { tod = "Night"; }
    else if ( t >= sun[s][ss][0] ) { tod = "Day"; }
    else { tod = "Night"; }

    // Save new values
    localStorage.setItem("sun", s);
    localStorage.setItem("day", d);
    localStorage.setItem("year", y);
    localStorage.setItem("time", t);
    // Export data
    $("#time-hour").html( Math.floor( t / 60 ) );
    $("#time-minute").html( t % 60 );
    $("#date-year-name").html( year_name[ y ] );
    $("#date-season").html( season );
    $("#date-tod").html( tod );
    if ( year[d][4] != "" ) { $("#date-event").html( "&bull; " + year[d][4] ); }
    else { $("#date-event").html(""); }
    if ( year[d][0] == "0th" ) {
      $("#date-day").html("");
      $("#date-month").html( year[d][1] );
    } else {
      $("#date-day").html( year[d][0] );
      $("#date-month").html( "of " + year[d][1] );
    }
    $("#date-year").html( y );
  }
  // Append this method to its triggers
  $("#cal-day, #cal-year, #cal-time").on("change input", updateTime);

